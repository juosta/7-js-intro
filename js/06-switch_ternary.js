// SWITCH STATEMENT
// Switch() { case, break, default }

const animal = "cat";

switch (animal) {
    case 'dog':
        console.log('Bark bark');
        break;
    case 'cat':
        console.log('Meow meow');
        break;
    default:
        console.log('Unknown animal');
        break;
}
